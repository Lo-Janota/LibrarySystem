import javax.swing.*;
import java.awt.*;
import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;

public class Menu extends JFrame {
    private JTextField searchField; // Campo de texto para pesquisa
    private JButton searchButton; // Bot√£o de pesquisa
    private JButton addButton; // Bot√£o de adicionar livro
    private JButton deleteButton; // Bot√£o de excluir livro
    private JButton configButton; // Bot√£o de configura√ß√£o
    private JButton editButton; // Bot√£o de editar
    private JTextArea bookListArea; // √Årea de texto para listar os livros
    private Biblioteca biblioteca; // Inst√¢ncia da classe Biblioteca para gerenciar os livros
    private ConfigUsers ConfigUsers; // Painel de configura√ß√£o

    public Menu() {
        setTitle("Menu Biblioteca"); // Define o t√≠tulo da janela
        setSize(700, 400); // Define o tamanho da janela
        setDefaultCloseOperation(EXIT_ON_CLOSE); // Define a opera√ß√£o padr√£o ao fechar a janela
        setLocationRelativeTo(null); // Define a posi√ß√£o da janela como centralizada na tela
        setLayout(new BorderLayout()); // Define o layout da janela como BorderLayout

        biblioteca = new Biblioteca(); // Inicializa a inst√¢ncia da classe Biblioteca
        searchField = new JTextField(20); // Cria um campo de texto para pesquisa com tamanho 20
        searchButton = new JButton("üîé"); // Cria um bot√£o de pesquisa com √≠cone
        addButton = new JButton("‚ûï"); // Cria um bot√£o de adicionar livro com √≠cone
        deleteButton = new JButton("‚ùå"); // Cria um bot√£o de excluir livro com √≠cone
        configButton = new JButton("üîß"); // Cria um bot√£o de configura√ß√£o com √≠cone
        editButton = new JButton("‚ùì"); // Cria um bot√£o de editar com icone
        bookListArea = new JTextArea(); // Cria uma √°rea de texto para listar os livros
        bookListArea.setEditable(false); // Define a √°rea de texto como somente leitura

        JPanel topPanel = new JPanel(new BorderLayout()); // Cria um painel superior com layout BorderLayout
        JPanel searchPanel = new JPanel(new FlowLayout(FlowLayout.CENTER)); // Painel para centralizar a barra de pesquisa
        JPanel emptyPanel = new JPanel(); // Painel vazio para alinhar a barra de pesquisa √† esquerda

        searchPanel.add(searchField); // Adiciona o campo de pesquisa ao painel
        searchPanel.add(searchButton); // Adiciona o bot√£o de pesquisa ao painel
        searchPanel.add(addButton); // Adiciona o bot√£o de adicionar livro ao painel
        searchPanel.add(deleteButton); // Adiciona o bot√£o de excluir livro ao painel
        searchPanel.add(editButton); //Adiciona o bot√£o de editar livro ao painel

        topPanel.add(searchPanel, BorderLayout.CENTER); // Adiciona o painel de pesquisa ao centro do painel superior
        topPanel.add(configButton, BorderLayout.WEST); // Adiciona o bot√£o de configura√ß√£o √† esquerda do painel superior

        // Define o tamanho dos bot√µes -> passa uma variavel para pegar esse tamanho
        Dimension buttonSize = new Dimension(60, 30);
        searchButton.setPreferredSize(buttonSize);
        addButton.setPreferredSize(buttonSize);
        deleteButton.setPreferredSize(buttonSize);
        configButton.setPreferredSize(buttonSize);
        editButton.setPreferredSize(buttonSize);

        // Adiciona tooltips (legenda) aos bot√µes
        searchButton.setToolTipText("Pesquisar");
        addButton.setToolTipText("Adicionar");
        deleteButton.setToolTipText("Excluir");
        configButton.setToolTipText("Configura√ß√£o");
        editButton.setToolTipText("Editar");

        JScrollPane scrollPane = new JScrollPane(bookListArea); // Cria um JScrollPane para a √°rea de texto para listar os livros

        add(topPanel, BorderLayout.NORTH); // Adiciona o painel superior √† parte norte da janela
        add(scrollPane, BorderLayout.CENTER); // Adiciona a √°rea de texto com scroll √† parte central da janela

        // Adiciona um ActionListener ao bot√£o de configura√ß√£o
        configButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (ConfigUsers == null) {
                    ConfigUsers = new ConfigUsers(); // Cria o painel de configura√ß√£o se ainda n√£o existir
                }
                JOptionPane.showMessageDialog(null, ConfigUsers, "Configurar Usu√°rios", JOptionPane.PLAIN_MESSAGE); // Exibe o painel de configura√ß√£o em um JOptionPane
            }
        });

        // Adiciona um ActionListener ao bot√£o de pesquisa
        searchButton.addActionListener(new ActionListener() { // Adiciona um ActionListener ao bot√£o de pesquisa
            @Override
            public void actionPerformed(ActionEvent e) { // Sobrescreve o m√©todo actionPerformed
                String termoPesquisa = searchField.getText().toLowerCase(); // Obt√©m o termo de pesquisa do campo de texto e converte para min√∫sculas
                StringBuilder resultado = new StringBuilder(); // Cria um StringBuilder para armazenar o resultado da busca
                boolean livroEncontrado = false; // Vari√°vel para verificar se pelo menos um livro foi encontrado
                for (Livro livro : biblioteca.getLivros()) {
                    if (livro.getTitulo().toLowerCase().equals(termoPesquisa) || // Verifica se o t√≠tulo, autor, categoria ou ISBN do livro √© exatamente igual (ignorando mai√∫sculas e min√∫sculas) ao termo de pesquisa
                            livro.getAutor().toLowerCase().equals(termoPesquisa) ||
                            livro.getCategoria().toLowerCase().equals(termoPesquisa) ||
                            livro.getIsbn().toString().equals(termoPesquisa)) {
                        resultado.append(livro).append("\n"); // Adiciona o livro ao resultado se for encontrado
                        livroEncontrado = true; // Define que pelo menos um livro foi encontrado
                    }
                }
                if (!livroEncontrado) { // Se nenhum livro foi encontrado
                    JOptionPane.showMessageDialog(null, "Nenhum livro foi encontrado.", "Nenhum resultado", JOptionPane.INFORMATION_MESSAGE); // Exibe um JOptionPane com a mensagem
                } else {
                    bookListArea.setText(resultado.toString()); // Atualiza o texto da √°rea de texto com o resultado da busca
                }
            }
        });

        // Adiciona um ActionListener ao bot√£o de adicionar livro
        addButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String titulo = JOptionPane.showInputDialog("Digite o t√≠tulo do livro:");
                String autor = JOptionPane.showInputDialog("Digite o autor do livro:");
                String categoria = JOptionPane.showInputDialog("Digite a categoria do livro:");
                String isbnStr = JOptionPane.showInputDialog("Digite o numero ISBN do livro:");

                // Converte a String do ISBN para Integer
                Integer isbn = null;
                try {
                    isbn = Integer.parseInt(isbnStr);
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null, "O ISBN deve ser um n√∫mero inteiro.");
                    return; // Retorna para evitar adicionar o livro com um ISBN inv√°lido
                }

                // Valida√ß√£o dos campos
                if (titulo != null && !titulo.isEmpty() &&
                        autor != null && !autor.isEmpty() &&
                        categoria != null && !categoria.isEmpty() &&
                        isbn != null) {
                    biblioteca.adicionarLivro(new Livro(titulo, autor, categoria, isbn));
                    atualizarListaLivros();
                } else {
                    JOptionPane.showMessageDialog(null, "ATEN√á√ÉO!\nTodos os campos devem ser preenchidos.");
                }
            }
        });


        // Adiciona um ActionListener ao bot√£o de excluir livro
        deleteButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String termoPesquisa = searchField.getText().toLowerCase();
                Livro livroSelecionado = null;
                for (Livro livro : biblioteca.getLivros()) {
                    if (livro.getTitulo().toLowerCase().contains(termoPesquisa)) {
                        livroSelecionado = livro;
                        break; // Interrompe o loop ao encontrar o primeiro livro correspondente
                    }
                }
                if (livroSelecionado != null) {
                    biblioteca.removerLivro(livroSelecionado);
                    atualizarListaLivros();
                } else {
                    JOptionPane.showMessageDialog(null, "Livro n√£o encontrado.");
                }
            }
        });

        // Adiciona um ActionListener ao bot√£o de editar livro
        editButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String termoPesquisa = searchField.getText().toLowerCase();
                Livro livroSelecionado = null;
                for (Livro livro : biblioteca.getLivros()) {
                    if (livro.getTitulo().toLowerCase().contains(termoPesquisa)) {
                        livroSelecionado = livro;
                        break; // Interrompe o loop ao encontrar o primeiro livro correspondente
                    }
                }
                if (livroSelecionado != null) {
                    // Abre um JOptionPane para edi√ß√£o do livro selecionado
                    String novoTitulo = JOptionPane.showInputDialog("Editar T√≠tulo:", livroSelecionado.getTitulo());
                    String novoAutor = JOptionPane.showInputDialog("Editar Autor:", livroSelecionado.getAutor());
                    String novaCategoria = JOptionPane.showInputDialog("Editar Categoria:", livroSelecionado.getCategoria());
                    String novoIsbnStr = JOptionPane.showInputDialog("Editar ISBN: ", livroSelecionado.getIsbn());

                    // Converte a String do ISBN para Integer
                    Integer novoIsbn = null;
                    try {
                        novoIsbn = Integer.parseInt(novoIsbnStr);
                    } catch (NumberFormatException ex) {
                        JOptionPane.showMessageDialog(null, "O ISBN deve ser um n√∫mero inteiro.");
                        return; // Retorna para evitar editar o livro com um ISBN inv√°lido
                    }

                    // Valida√ß√£o dos campos
                    if (novoTitulo != null && !novoTitulo.isEmpty() &&
                            novoAutor != null && !novoAutor.isEmpty() &&
                            novaCategoria != null && !novaCategoria.isEmpty() &&
                            novoIsbn != null) {
                        // Atualiza as informa√ß√µes do livro selecionado
                        livroSelecionado.setTitulo(novoTitulo);
                        livroSelecionado.setAutor(novoAutor);
                        livroSelecionado.setCategoria(novaCategoria);
                        livroSelecionado.setIsbn(novoIsbn);
                        // Atualiza a lista de livros na interface gr√°fica
                        atualizarListaLivros();
                        JOptionPane.showMessageDialog(null, "Livro editado com sucesso!"); // Mensagem de sucesso
                    } else {
                        JOptionPane.showMessageDialog(null, "ATEN√á√ÉO!\nTodos os campos devem ser preenchidos.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Livro n√£o encontrado.");
                }
            }
        });


        atualizarListaLivros(); // Atualiza a lista de livros ao iniciar a aplica√ß√£o
    }

    // Atualiza a lista de livros na √°rea de texto
    private void atualizarListaLivros() {
        StringBuilder listaLivros = new StringBuilder();
        for (Livro livro : biblioteca.getLivros()) {
            listaLivros.append(livro).append("\n--------------------------------------------------------------------------------------------------------------------------------------------------------------------------\n");
        }
        bookListArea.setText(listaLivros.toString());
    }

    // M√©todo principal para executar a aplica√ß√£o Swing
    public static void main(String[] args) {
        SwingUtilities.invokeLater(() -> {
            Menu menu = new Menu();
            menu.setVisible(true);
        });
    }
}
